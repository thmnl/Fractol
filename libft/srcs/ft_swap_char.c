#include "libft.h"

char	*ft_swap_char(char *tab, char c, char r)
{
	int i;

	i = 0;
	while (tab[i] != '\0')
	{
		if (tab[i] == c)
			tab[i] = r;
		i++;
	}
	return (tab);
}
